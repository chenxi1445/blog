function renderPostList() {
  const posts = getPosts();

  if (posts.length === 0) {
    app.innerHTML = `
      <div class="posts-header">
        <h1>最新文章</h1>
      </div>
      <div class="empty-state">
        <h2>还没有文章</h2>
        <p>点击右上角 "新建文章" 开始写博客吧！</p>
      </div>
    `;
    return;
  }

  const cards = posts
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .map(post => `
      <a href="#/post/${post.id}" class="post-card">
        <h2>${escapeHtml(post.title)}</h2>
        <div class="meta">${formatDate(post.date)}</div>
        <div class="excerpt">${escapeHtml(getExcerpt(post.content))}</div>
      </a>
    `)
    .join('');

  app.innerHTML = `
    <div class="posts-header">
      <h1>最新文章</h1>
    </div>
    <div class="post-list">${cards}</div>
  `;
}

