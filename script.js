// ========== 渲染：表单（新建/编辑） ==========

function renderForm(id) {
  const isEdit = !!id;
  const post = isEdit ? getPostById(id) : null;

  if (isEdit && !post) {
    navigate('#/');
    return;
  }

  const title = post ? escapeHtml(post.title) : '';
  const content = post ? escapeHtml(post.content) : '';
  const heading = isEdit ? '编辑文章' : '新建文章';

  app.innerHTML = `
    <div class="post-form">
      <h1>${heading}</h1>
      <div class="form-group">
        <label for="post-title">标题</label>
        <input id="post-title" type="text" placeholder="输入文章标题..." value="${title}">
      </div>
      <div class="form-group">
        <label for="post-content">内容</label>
        <textarea id="post-content" placeholder="支持 Markdown 语法...">${content}</textarea>
        <div class="hint">支持 Markdown 语法：**粗体** *斜体* \`代码\` [链接](url) ## 标题</div>
      </div>
      <div class="form-actions">
        <button class="btn btn-primary" onclick="savePost('${isEdit ? id : ''}')">
          ${isEdit ? '保存修改' : '发布文章'}
        </button>
        <a href="${isEdit ? '#/post/' + id : '#/'}" class="btn btn-secondary">取消</a>
      </div>
    </div>
  `;
}

// ========== 操作：保存文章 ==========

function savePost(id) {
  const titleInput = document.getElementById('post-title');
  const contentInput = document.getElementById('post-content');

  const title = titleInput.value.trim();
  const content = contentInput.value.trim();

  if (!title) {
    alert('请输入文章标题');
    titleInput.focus();
    return;
  }

  if (!content) {
    alert('请输入文章内容');
    contentInput.focus();
    return;
  }

  const posts = getPosts();

  if (id) {
    // 编辑
    const index = posts.findIndex(p => p.id === id);
    if (index !== -1) {
      posts[index].title = title;
      posts[index].content = content;
      savePosts(posts);
      navigate('#/post/' + id);
    }
  } else {
    // 新建
    const newPost = {
      id: generateId(),
      title,
      content,
      date: new Date().toISOString()
    };
    posts.push(newPost);
    savePosts(posts);
    navigate('#/post/' + newPost.id);
  }
}

// ========== 操作：删除文章 ==========

function deletePost(id) {
  if (!confirm('确定要删除这篇文章吗？此操作不可撤销。')) return;

  const posts = getPosts().filter(p => p.id !== id);
  savePosts(posts);
  navigate('#/');
}
