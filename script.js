function getPosts() {
  const raw = localStorage.getItem(STORAGE_KEY);
  let posts = raw ? JSON.parse(raw) : [];

  // 合并种子文章：始终确保种子文章存在，同时也会更新已有种子内容
  const seeds = getSeedPosts();
  let changed = false;
  for (const seed of seeds) {
    const idx = posts.findIndex(p => p.id === seed.id);
    if (idx === -1) {
      posts.push(seed);
      changed = true;
    } else {
      posts[idx] = seed;
      changed = true;
    }
  }

  if (changed) {
    savePosts(posts);
  }

  return posts;
}

function savePosts(posts) {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(posts));
}

function getPostById(id) {
  return getPosts().find(p => p.id === id);
}

// ========== 工具函数 ==========

function formatDate(dateStr) {
  return new Date(dateStr).toLocaleDateString('zh-CN', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  });
}

function generateId() {
  return Date.now().toString(36) + Math.random().toString(36).slice(2, 8);
}

function stripMarkdown(md) {
  return md
    .replace(/#{1,6}\s/g, '')
    .replace(/\*\*(.+?)\*\*/g, '$1')
    .replace(/\*(.+?)\*/g, '$1')
    .replace(/`(.+?)`/g, '$1')
    .replace(/\[([^\]]+)\]\([^)]+\)/g, '$1')
    .replace(/!\[([^\]]*)\]\([^)]+\)/g, '$1')
    .replace(/>\s/g, '')
    .replace(/[-*+]\s/g, '')
    .replace(/\n{2,}/g, '\n')
    .trim();
}

function escapeHtml(str) {
  const div = document.createElement('div');
  div.textContent = str;
  return div.innerHTML;
}

// 获取文章摘要（纯文本前 150 字）
function getExcerpt(content) {
  const text = stripMarkdown(content);
  return text.length > 150 ? text.slice(0, 150) + '...' : text;
}

// ========== 路由 ==========

const app = document.getElementById('app');

function parseHash() {
  const hash = location.hash.slice(1) || '/';
  const parts = hash.split('/').filter(Boolean);

  if (parts.length === 0 || parts[0] === '') {
    return { page: 'list' };
  }
  if (parts[0] === 'new') {
    return { page: 'new' };
  }
  if (parts[0] === 'edit' && parts[1]) {
    return { page: 'edit', id: parts[1] };
  }
  if (parts[0] === 'post' && parts[1]) {
    return { page: 'post', id: parts[1] };
  }
  return { page: 'list' };
}

function navigate(hash) {
  location.hash = hash;
}

function router() {
  const route = parseHash();

  switch (route.page) {
    case 'list':
      renderPostList();
      break;
    case 'new':
      renderForm();
      break;
    case 'edit':
      renderForm(route.id);
      break;
    case 'post':
      renderPostDetail(route.id);
      break;
    default:
      renderPostList();
  }
}

window.addEventListener('hashchange', router);
window.addEventListener('load', function() {
  try {
    router();
  } catch(e) {
    document.getElementById('app').innerHTML =
      '<div class=\"empty-state\"><h2>加载出错</h2><p>' + e.message + '</p>' +
      '<p style=\"margin-top:16px\"><button class=\"btn btn-primary\" onclick=\"localStorage.clear();location.reload()\">清除数据并刷新</button></p></div>';
  }
});

